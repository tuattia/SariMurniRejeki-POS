    public void searchBarang(String keyword) {
    DefaultTableModel model = (DefaultTableModel) tablebarang.getModel();
    model.setRowCount(0);

    String sql = "SELECT * FROM barang WHERE kode_barang LIKE ? OR nama_barang LIKE ?";

    try {
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, "%" + keyword + "%");
        ps.setString(2, "%" + keyword + "%");

        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            Object[] row = {
                rs.getString("kode_barang"),
                rs.getString("nama_barang"),
                rs.getInt("harga_jual"),
                rs.getInt("stok"),
                rs.getString("satuan")
            };
            model.addRow(row);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error search data: " + e.getMessage());
    }
}

private void loadTable() {
    model = (DefaultTableModel) tablebarang.getModel();
    model.setRowCount(0);

    for (modelbarang b : bc.tampil()) {
        Object[] row = {
            b.getKodeBarang(),
            b.getNamaBarang(),
            b.getHargaJual(),
            b.getStok(),
            b.getSatuan()
        };
        model.addRow(row);
    }
}

   

