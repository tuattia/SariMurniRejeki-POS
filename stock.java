    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
    
    if (!validasiForm()) return;

    modelbarang b = new modelbarang();
    b.setKodeBarang(txtkode.getText());
    b.setNamaBarang(txtbarang.getText());
    b.setHargaJual(Integer.parseInt(txtharga.getText()));
    b.setStok(Integer.parseInt(txtjumlah.getText()));
    b.setSatuan(txtsatuan.getText());

    int hasil = bc.tambahBarang(b);

    if (hasil > 0) {
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
        clearForm();
        loadTable();
    } else {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data");
    }    
       
    }//GEN-LAST:event_btnsaveActionPerformed

