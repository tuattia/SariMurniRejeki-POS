    public void searchUtang(String keyword) {
    DefaultTableModel model = (DefaultTableModel) Tableutang.getModel();
    model.setRowCount(0);

    String sql = "SELECT * FROM utang WHERE kode_utang LIKE ? OR nama LIKE ?";

    try {
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, "%" + keyword + "%");
        ps.setString(2, "%" + keyword + "%");

        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            Object[] row = {
                rs.getString("kode_utang"),
                rs.getString("nama"),
                rs.getString("alamat"),
                rs.getString("telepon"),
                rs.getInt("jumlah_cicilan"),
                rs.getDate("jatuh_tempo")
            };
            model.addRow(row);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error search data: " + e.getMessage());
    }
}

